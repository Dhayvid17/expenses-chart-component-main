const bars = document.querySelectorAll(".mon-bar, .tue-bar, .wed-bar, .thur-bar, .fri-bar, .sat-bar, .sun-bar");
const paras = document.querySelectorAll(".days-bar p");

bars[0].addEventListener("click", function () {

    if (paras[0].style.display === "none") {
        paras[0].style.display = "block";
    } else {
        paras[0].style.display = "none";
    }
});

